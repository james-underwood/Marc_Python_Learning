# Introduction to Lists
# Lists are used to store multiple items in a single variable. They can hold different data types and can be modified.

# Example usage:
fruits = ["apple", "banana", "orange"]

print(fruits[0])  # Accessing the first element
print(len(fruits))  # Getting the length of the list

fruits.append("grape")  # Adding a new element to the end of the list
fruits.remove("banana")  # Removing an element from the list

print(fruits)  # Printing the modified list

