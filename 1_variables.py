# Introduction to Variables
# Variables are used to store data in Python. They can hold different types of values, such as numbers or text.

# Example usage:
name = "John"
age = 25
height = 1.75

print("My name is", name)
print("I am", age, "years old")
print("My height is", height, "meters")

